#include <stdio.h>

long long	bin[91] = { 0, 1, 1, 2, 3, 5, 8, 13, };

long long	binary(int b)
{
	if (bin[b] != 0)
		return (bin[b]);
	else
	{
		bin[b] = binary(b - 1) + binary(b - 2);
		return (bin[b]);
	}
}

int		main()
{
	int b;
	scanf("%d", &b);
	printf("%lld\n", binary(b));
}

